        // Function to update dropdown
        function updateDropdown() {
            const dropdown = document.getElementById('languageDropdown');
            dropdown.innerHTML = '';

            if (currentLang === 'en') {
                // Show other languages
                const langs = [
                    { code: 'hr', name: 'Hrvatski' },
                    { code: 'de', name: 'Deutsch' },
                    { code: 'it', name: 'Italiano' },
                    { code: 'es', name: 'Español' },
                    { code: 'fr', name: 'Français' }
                ];
                langs.forEach(lang => {
                    const a = document.createElement('a');
                    a.href = '#';
                    a.textContent = lang.name;
                    a.setAttribute('data-lang', lang.code);
                    dropdown.appendChild(a);
                });
            } else {
                // Show English first, then others
                const aEn = document.createElement('a');
                aEn.href = '#';
                aEn.textContent = 'English';
                aEn.setAttribute('data-lang', 'en');
                dropdown.appendChild(aEn);

                const otherLangs = [
                    { code: 'hr', name: 'Hrvatski' },
                    { code: 'de', name: 'Deutsch' },
                    { code: 'it', name: 'Italiano' },
                    { code: 'es', name: 'Español' },
                    { code: 'fr', name: 'Français' }
                ].filter(lang => lang.code !== currentLang);

                otherLangs.forEach(lang => {
                    const a = document.createElement('a');
                    a.href = '#';
                    a.textContent = lang.name;
                    a.setAttribute('data-lang', lang.code);
                    dropdown.appendChild(a);
                });
            }
        }

        // Event listeners
        document.getElementById('translateBtn').addEventListener('click', function(e) {
            e.preventDefault();
            const dropdown = document.getElementById('languageDropdown');
            dropdown.style.display = dropdown.style.display === 'block' ? 'none' : 'block';
        });

        document.getElementById('languageDropdown').addEventListener('click', function(e) {
            if (e.target.tagName === 'A') {
                e.preventDefault();
                const lang = e.target.getAttribute('data-lang');
                setLanguage(lang);
                document.getElementById('languageDropdown').style.display = 'none';
            }
        });

        // Close dropdown when clicking outside
        document.addEventListener('click', function(e) {
            const btn = document.getElementById('translateBtn');
            const dropdown = document.getElementById('languageDropdown');
            if (!btn.contains(e.target) && !dropdown.contains(e.target)) {
                dropdown.style.display = 'none';
            }
        });

